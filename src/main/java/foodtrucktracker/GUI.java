    private void updateTruckBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateTruckBtnActionPerformed
        // TODO add your handling code here:
        String posText = positionTF.getText();

        if (posText.isEmpty()) {
            displayTextArea.setText("Please enter a position to update.");
        } else {
            int pos = Integer.parseInt(posText);
            if (pos < 1 || pos > truckList.size()) {
                displayTextArea.setText("Invalid position. List has " + truckList.size() + " trucks.");
            } else {
                String name = truckNameTF.getText();
                String cuisine = cuisineTF.getText();
                String location = locationTF.getText();
                String owner = ownerTF.getText();
                String reg = regNoTF.getText();
                FoodTruck updated = new FoodTruck(reg, owner, name, cuisine, location);
                truckList.update(pos, updated);
                clearFields();
                displayTextArea.setText("Truck at position " + pos + " updated successfully!");
            }
        }
    }//GEN-LAST:event_updateTruckBtnActionPerformed

