    private void deleteTruckBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteTruckBtnActionPerformed
        // TODO add your handling code here:
        String name = truckNameTF.getText();
        boolean found = false;

        for (int i = 1; i <= truckList.size(); i++) {
            FoodTruck ft = (FoodTruck) truckList.get(i);
            if (ft.getTruckName().equalsIgnoreCase(name)) {
                truckList.remove(i);
                clearFields();
                displayTextArea.setText(name + " removed successfully!\nTotal trucks: " + truckList.size());
                found = true;
                break;
            }
        }
        if (!found) {
            displayTextArea.setText(name + " not found in the system.");
        }
    }//GEN-LAST:event_deleteTruckBtnActionPerformed

