    private void addTruckBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addTruckBtnActionPerformed
        // TODO add your handling code here:
        String name = truckNameTF.getText();
        String cuisine = cuisineTF.getText();
        String location = locationTF.getText();
        String owner = ownerTF.getText();
        String reg = regNoTF.getText();
       

        if (name.isEmpty() || cuisine.isEmpty() || location.isEmpty() || owner.isEmpty() || reg.isEmpty()) {
            displayTextArea.setText("Please fill in all fields!");
        } else {
            FoodTruck ft = new FoodTruck(reg, owner, name, cuisine, location);
            truckList.add(ft);
            truckStack.push(ft);
            clearFields();
            displayTextArea.setText("Truck added successfully!\nTotal trucks: " + truckList.size());
        }
    }//GEN-LAST:event_addTruckBtnActionPerformed

